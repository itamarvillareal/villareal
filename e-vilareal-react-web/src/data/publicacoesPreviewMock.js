function montarMetricas(itens) {
  const n = itens.length;
  const comCnj = itens.filter((x) => x.processoCnjNormalizado).length;
  const semTeor = itens.filter((x) => x.statusTeor === 'vazio').length;
  const indisponivel = itens.filter(
    (x) => x.statusTeor !== 'integral' && x.statusTeor !== 'vazio'
  ).length;
  const consultasFalha = itens.filter((x) => x.statusValidacaoCnj === 'consulta_indisponivel').length;

  return {
    blocosDetectados: n,
    comCnj,
    semTeor,
    indisponivel,
    duplicatasDescartadas: 0,
    blocosAposDedup: n,
    confirmadosDatajud: 0,
    naoConfirmadosDatajud: 0,
    consultasComErroRede: consultasFalha,
    consultasDatajudPuladas: 0,
    vinculadosInterno: itens.filter((x) => x.statusVinculo === 'vinculado').length,
    vinculosEncontrados: itens.filter((x) => x.statusVinculo === 'vinculado').length,
    naoVinculados: itens.filter((x) => x.statusVinculo === 'nao_vinculado' || x.statusVinculo === 'sem_cnj')
      .length,
  };
}

function montarLogsItens(itens) {
  return itens.map((x) => ({
    indice: x.indiceBloco,
    cnj: x.processoCnjNormalizado || null,
    datajudMotivo:
      x.statusValidacaoCnj === 'nao_consultado' ? 'camada2_desligada' : 'rede',
    divergencias: undefined,
  }));
}

